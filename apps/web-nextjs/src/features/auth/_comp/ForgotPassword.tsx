import { useRouter, useSearchParams } from 'next/navigation'
import { FormEvent, useEffect, useState } from 'react'
import z from 'zod'
import { OTPSchema, passwordSchema, phoneSchema } from '@/features/auth/type'
import { useForm } from 'react-hook-form'
import { zodResolver } from '@hookform/resolvers/zod'
import { useCountdown } from 'usehooks-ts'
import { authClient } from '@/features/auth/client'
import { toast } from '@/app/a/ui/toast'
import { FormField } from '@/app/a/ui/form/FormField'
import { Field, FieldDescription, FieldError } from '@/components/ui/field'
import {
  InputGroup,
  InputGroupAddon,
  InputGroupInput,
  InputGroupText,
} from '@/components/ui/input-group'
import { Hash, Smartphone } from 'lucide-react'
import { InputPassword } from '@/app/(main)/@modal/_comp/InputPassword'
import { Button } from '@/components/uix/html'

const formSchema = z
  .object({
    phoneNumber: phoneSchema,
    password: passwordSchema,
    confirmPassword: z.string().min(1, '请确认密码'),
    code: OTPSchema,
  })
  .refine(data => data.password === data.confirmPassword, {
    message: '两次输入的密码不一致',
    path: ['confirmPassword'],
  })
type Data = z.infer<typeof formSchema>
// forget
export function ForgotPassword() {
  const router = useRouter()
  const searchParams = useSearchParams()
  const callbackUrl = searchParams?.get('callbackUrl') || '/'
  const { formState, handleSubmit, control, trigger, setFocus, getValues } =
    useForm<Data>({
      resolver: zodResolver(formSchema),
      mode: 'onChange',
    })
  // useCountdown 配置：初始 60s，成功发送后启动
  const [sending, setSending] = useState(false)
  const [count, { startCountdown, stopCountdown, resetCountdown }] = useCountdown({
    countStart: 60,
    intervalMs: 1000,
  })
  useEffect(() => {
    if (count === 0) setSending(false)
  }, [count])
  const onSubmit = async (fd: Data) => {
    const { data, error } = await authClient.phoneNumber.resetPassword({
      otp: fd.code,
      phoneNumber: `+86${fd.phoneNumber}`,
      newPassword: fd.password,
    })
    if (error) {
      toast.error(error)
      return
    }
    toast.success('密码重置成功')
    router.push(callbackUrl)
  }
  const onSendPhoneOtp = async () => {
    if (sending) return // 避免重复发送
    const isValid = await trigger('phoneNumber')
    if (!isValid) {
      // 可选: toast 错误或聚焦字段
      setFocus('phoneNumber')
      // toast.error("手机号格式无效");
      return // 验证失败，停止发送
    }
    resetCountdown()
    setSending(true)
    startCountdown()
    const { data, error } = await authClient.phoneNumber.requestPasswordReset({
      phoneNumber: `+86${getValues('phoneNumber')}`,
    })
    if (error) {
      toast.error(error)
      setSending(false)
      return
    }
  }
  return (
    <form className="space-y-3" onSubmit={handleSubmit(onSubmit)}>
      <FormField
        name="phoneNumber"
        control={control}
        render={({ field, fieldState }) => (
          <InputGroup>
            <InputGroupInput
              {...field}
              aria-invalid={fieldState.invalid}
              placeholder="请输入手机号"
              className="bg-transparent! dark:bg-transparent!"
            />
            <InputGroupAddon>
              <Smartphone />
              <InputGroupText>+86</InputGroupText>
            </InputGroupAddon>
          </InputGroup>
        )}
      />
      <FormField
        name="password"
        control={control}
        render={({ field, fieldState }) => (
          <InputPassword {...field} aria-invalid={fieldState.invalid} />
        )}
      />
      <FormField
        name="confirmPassword"
        control={control}
        render={({ field, fieldState }) => (
          <InputPassword
            {...field}
            aria-invalid={fieldState.invalid}
            placeholder="请再次输入密码"
          />
        )}
      />
      <FormField
        name="code"
        control={control}
        render={({ field, fieldState }) => (
          <>
            <div className="flex gap-2">
              <InputGroup>
                <InputGroupInput
                  {...field}
                  aria-invalid={fieldState.invalid}
                  placeholder="请输入验证码"
                />
                <InputGroupAddon>
                  <Hash />
                </InputGroupAddon>
              </InputGroup>
              <Button
                className="w-25"
                type="button"
                variant="secondary"
                disabled={sending}
                onClick={() => onSendPhoneOtp()}
              >
                {sending ? `${count} 秒后重发` : '发送验证码'}
              </Button>
            </div>
            <FieldDescription>5分钟内有效, 隔60秒可重新发送</FieldDescription>
          </>
        )}
      />
      <Button pending={formState.isSubmitting} type="submit" className="w-full mt-6">
        重置密码
      </Button>
    </form>
  )
}
